#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i] != '\0')
	{
		i = i + 1;
	}
	while (s2[j] != '\0' && n != 0)
	{
		s1[i] = s2[j];
		i = i + 1;
		j = j + 1;
		n = n - 1;
	}
	s1[i] = '\0';
	return (s1);
}
